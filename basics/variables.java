public class Variables {
    public static void main(String[] args) {
        int myAge = 24; 
        String message = "Hello, User!";
        int userAge = myAge;
        myAge = 25;
        
        System.out.println("Message: " + message); 
        System.out.println("My age: " + myAge);
        System.out.println("Your age: " + userAge);

    }

}